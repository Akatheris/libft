#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			n;
	char			*str;

	if (s == NULL || f == NULL)
		return (NULL);
	i = 0;
	n = ft_strlen(s);
	if ((str = ft_strnew(n)) == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	return (str);
}
